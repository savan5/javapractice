package calculation;

public class useOfFloat {
    static int c=55;
    static float a=22.05f;
static double d=3.005;
    public static void main(String[] args) {
        System.out.println(a);
        System.out.println(a+c);
        System.out.println(a+c+d);
        System.out.println(d);
    }
}
